// Simple translate helper that takes siteLanguage, and a Dutch key/text string.
// If the selected language is Dutch or the key is not in the translations, it returns the key as is.
export function translateUI(siteLanguage: string, key: string): string {
  if (!key) return "";
  const lang = (siteLanguage || "NL").toUpperCase();
  if (lang === "NL" || lang === "NEDERLANDS") {
    return key;
  }
  
  const translationsForLang = UI_TRANSLATIONS[lang];
  if (translationsForLang && translationsForLang[key]) {
    return translationsForLang[key];
  }

  // Exact fallback check
  const trimmed = key.trim();
  if (translationsForLang && translationsForLang[trimmed]) {
    return translationsForLang[trimmed];
  }

  return key;
}
